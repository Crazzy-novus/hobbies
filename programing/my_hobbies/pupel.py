import turtle
a=turtle.Turtle()
turtle.bgcolor(0.5,0.3,0.5)
a.pensize(3)
a.begin_fill()
a.fillcolor('black')
a.pencolor("pink")
a.fd(50)
a.pencolor("red")
a.fd(50)
def box():
    for i in range(90):
        a.right(1)
        a.fd(1)
box()
a.fd(50)
a.pencolor("yellow")
a.fd(50)
box()
a.fd(50)
a.pencolor('orange')
a.fd(50)
box()
a.fd(50)
a.pencolor("pink")
a.fd(50)
for i in range(90):
    a.right(1)
    a.fd(1)
a.end_fill()
a.up()
a.goto(27,-180)
a.down()
a.pensize(15)
a.pencolor('white')
a.left(90)
a.fd(140)
a.right(90)
a.fd(35)
for i in range(120):
        a.right(1.5)
        a.fd(1)
a.fd(33)
a.hideturtle()

turtle.done()